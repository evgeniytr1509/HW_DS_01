import numpy as np
#18 Створіть вектор розміром 4, заповніть його випадковими цілими числами 
# в діапазоні від 1 до 50 та знайдіть вектор з квадратними коренями цих чисел.

vector = np.random.randint(1, 50, size=4)

# Виводимо початковий вектор
print("Початковий вектор:")
print(vector)

# Обчислюємо вектор з квадратними коренями чисел
sqrt_vector = np.sqrt(vector)

# Виводимо вектор з квадратними коренями
print("Вектор з квадратними коренями:")
print(sqrt_vector)