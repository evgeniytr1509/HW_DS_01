import numpy as np
#17 Створіть матрицю розміром 3x4 з довільними цілими числами і створінь матрицю з квадратами цих чисел. 

matrix = np.random.randint(1, 10, size=(3, 4))  # Генеруємо числа від 1 до 10

# Виводимо початкову матрицю
print("Початкова матриця:")
print(matrix)

# Створюємо матрицю, яка містить квадрати цих чисел
squared_matrix = np.square(matrix)

# Виводимо матрицю з квадратами чисел
print("Матриця з квадратами чисел:")
print(squared_matrix)
